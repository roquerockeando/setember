                <h5 style="color: #00838f">Back-Ends</h5>
                <br>
                São os programadores que agem "por trás dos panos", onde eles mexem com algo não tão visual, <br>
                mas sim com a lógica do programa em si. <br> Um exemplo, quando vamos entrar com uma conta no Facebook, você vê a parte visual, <br> entra com sua conta e tudo não acontece por mágica. <br>
                São os Back-Ends que fizeram um sistema para que você conseguisse acessar sua conta.

                <br>
                <br>

                <h5 style="color: #8bc34a">Front-Ends</h5>
                <br>
                São os programadores que agem na frente, na parte visual de tudo. <br>
                Eles são responsáveis pelas animações, design do projeto e até uma questão importante. <br>
                A facilidade de uso do aplicativo ou projeto. <br> Um exemplo é um site que você acessa pelo seu computador de uma forma bonita e agradável, e no seu celular é automáticamente adaptado. <br>
